    private void tableCategoriaMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoriaMouseReleased
        if(tableCategoria.getSelectedRow() != -1){
            txtNomeCategoria.setText(tableCategoria.getValueAt(tableCategoria.getSelectedRow(), 1).toString());
            cbbTipoCat.setSelectedItem(tableCategoria.getValueAt(tableCategoria.getSelectedRow(), 2).toString());
            txtNomeCategoria.setEnabled(false);
            cbbTipoCat.setEnabled(false);
            btnCadastrar.setEnabled(false);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnNovo.setEnabled(false);
            btnVoltar.setEnabled(true);
        }
    }//GEN-LAST:event_tableCategoriaMouseReleased

