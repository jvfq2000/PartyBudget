    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Categoria novaCat = new Categoria();
        
        if (txtNomeCategoria.getText().equals("") || cbbTipoCat.getSelectedIndex() == 0) {
            Mensagem.camposObrigatorios();
            
        } else if(btnCadastrar.getText().equals("Cadastrar")){
            novaCat.cadastrarOuAlterar(txtNomeCategoria.getText(), cbbTipoCat.getSelectedItem().toString());
            
            BaseDados.listaCat.add(novaCat);
            int ultPos = BaseDados.listaCat.size() - 1;
            
            dtm.addRow(new Object[]{
                BaseDados.listaCat.get(ultPos).getCODIGO(),
                BaseDados.listaCat.get(ultPos).getNome(),
                BaseDados.listaCat.get(ultPos).getTipoCategoria(),
                jCheckBox1
            });
            
            camposInicio();
            Mensagem.sucessoCadastro();
        
        } else {
            int posCat = tableCategoria.getSelectedRow();
            
            BaseDados.listaCat.get(posCat).cadastrarOuAlterar(txtNomeCategoria.getText(),
                    cbbTipoCat.getSelectedItem().toString());
            
            dtm.setValueAt(BaseDados.listaCat.get(posCat).getCODIGO(), posCat, 0);
            dtm.setValueAt(BaseDados.listaCat.get(posCat).getNome(), posCat, 1);
            dtm.setValueAt(BaseDados.listaCat.get(posCat).getTipoCategoria(), posCat, 2);
            
            btnCadastrar.setText("Cadastrar");
            camposInicio();
            Mensagem.sucessoAlteracao();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

